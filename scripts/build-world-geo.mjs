/**
 * Bakes Natural Earth 110m land into pre-projected SVG path data for the
 * survey-basemap instrument (home Global teaser + Global page hero).
 *
 * Projection: equirectangular into a 360×180 space — x = lon + 180,
 * y = 90 − lat. This keeps the market-node math (lon/lat → x/y) trivial and
 * makes the coastline line up exactly with the plotted markets. Output is a
 * static string module: no d3-geo / topojson in the client bundle.
 *
 * Run:  node scripts/build-world-geo.mjs
 */
import { feature } from "topojson-client";
import land from "world-atlas/land-110m.json" with { type: "json" };
import { writeFileSync } from "node:fs";

const geo = feature(land, land.objects.land);

// Equirectangular into 0..360 (lon) × 0..180 (lat-down) space.
const px = (lon) => +(lon + 180).toFixed(1);
const py = (lat) => +(90 - lat).toFixed(1);

/** One ring [[lon,lat]...] -> "M x y L x y ... Z" */
function ring(coords) {
  let d = "";
  for (let i = 0; i < coords.length; i++) {
    const [lon, lat] = coords[i];
    d += `${i === 0 ? "M" : "L"}${px(lon)} ${py(lat)}`;
  }
  return d + "Z";
}

const paths = [];
for (const f of geo.features) {
  const g = f.geometry;
  const polys = g.type === "Polygon" ? [g.coordinates] : g.coordinates;
  for (const poly of polys) for (const r of poly) paths.push(ring(r));
}

const LAND = paths.join("");

const out = `/**
 * AUTO-GENERATED by scripts/build-world-geo.mjs — do not edit by hand.
 *
 * Natural Earth 110m land, projected equirectangular into a 360×180 space
 * (x = lon + 180, y = 90 − lat). Powers the survey-basemap instrument.
 * Re-run the script to regenerate. Source: world-atlas (Natural Earth,
 * public domain).
 */

/** Full land silhouette as a single SVG path 'd' (fill-rule: evenodd). */
export const WORLD_LAND_PATH =
  "${LAND}";

/** Project geographic coordinates into the baked 360×180 path space. */
export const lonToX = (lon: number) => lon + 180;
export const latToY = (lat: number) => 90 - lat;

/**
 * Recommended viewBox: full longitude, latitudes ~78°N → ~56°S. Trims the
 * Antarctic sprawl and empty high Arctic while keeping every regulated market
 * (northernmost ≈ 56°N, southernmost ≈ 41°S) comfortably in frame.
 */
export const WORLD_VIEWBOX = { x: 0, y: 12, w: 360, h: 134 } as const;
export const WORLD_VIEWBOX_STR = "0 12 360 134";
`;

writeFileSync(new URL("../src/content/worldGeo.ts", import.meta.url), out);
console.log(`worldGeo.ts written — land path ${(LAND.length / 1024).toFixed(1)} KB, ${paths.length} rings`);
